package Streams.Filter.DesafioFilter;

public class Carros {

    final String nome;
    final double preco;
    final boolean quatroPortas;
    final boolean turbo;

    public Carros(String nome, double preco, boolean quatroPortas, boolean turbo) {
        this.nome = nome;
        this.preco = preco;
        this.quatroPortas = quatroPortas;
        this.turbo = turbo;
    }
}
